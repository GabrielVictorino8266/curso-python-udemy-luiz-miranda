# Operador IN e NOT IN

# 0 1 2 3 4 5
# o t a v i o
# -6-5-4-3-2-1

nome = "Otavio"
# Uma string se trata de uma lista de CHAR.

#Uma string e iteravel
print(nome[2])

print('Ota' in nome) # Confere se 'Ota' esta no nome "Otavio"
print('vi' in nome) # Confere se 'vi' esta no nome "Otavio"
print('A' in nome) # Confere se 'O' esta no nome "Otavio"